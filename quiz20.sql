--THE BELOW WRITTEN ARE  NUMBER FUNCTIONS IN SQL
-- to display the absolute value.
select abs(-1);
-- to display the reminder
select mod(10,3);
-- to display rounded value
select round(22.22226,0);
-- The TRUNC() function truncates a number to the specified number of decimal places without rounding.
select trunc(12.262,2);
-- to natural log of number
select log(2);  -- Result: 1 (approx.)
